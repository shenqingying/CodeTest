CREATE PROCEDURE [dbo].[LE_TRA_City_Update]
	@ZDID int = 0,
    @ZDFLID int = 0, 
    @ZDMC varchar(100) = ' ',
    @ZDSM varchar(500) = ' '
AS
BEGIN

	IF @ZDFLID>0
	BEGIN	
		UPDATE SP_TYZD
		SET ZDMC=@ZDMC,ZDSM =@ZDSM 
		WHERE ZDID = @ZDID;
	END	
	ELSE
	BEGIN
		UPDATE SP_ZDFL
		SET FLMC=@ZDMC,FLSM =@ZDSM 
		WHERE ZDFLID = @ZDID;
	END	
	
	SELECT @ZDID ID;	
END
